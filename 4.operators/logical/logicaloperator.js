// && (logical AND)both conditions must follow
var age= 35;
if(age >= 18 && age <=40){console.log("this is true")};

// || (logical OR)atleast one condition should follow
var a=10;
var b=10;
if(a == 5 || b == 10){console.log("this is true")};

// ! (logical NOT)it will show true if the operand is false
var l=5;
if(!l == 5 ){console.log("this is true")};